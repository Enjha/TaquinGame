package ia;

public class VoidCase {
    private final int posX;
    private final int posY;

    public VoidCase(int posX, int posY){
        this.posX = posX;
        this.posY = posY;
    }

    public int getPosX() {
        return posX;
    }
    public int getPosY() {
        return posY;
    }
}
